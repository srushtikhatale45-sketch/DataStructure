package datastructure;

public class IfElse {

	public static void main(String[] args) {
		        int marks = 55;
		        if(marks>=90){
		            System.out.println("Grade A");
		        }else if(marks>=77){
		            System.out.println("Grade B");
		        }else if(marks>=50){
		            System.out.println("Grade C");
		        }else if(marks>=35){
		            System.out.println("Grade D");
		        }else {
		            System.out.println("Fail");
		        }
		      }
		    

}
