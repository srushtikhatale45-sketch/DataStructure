package datastructure;

public class Strings {

	public static void main(String[] args) {
		String greeting = "Hello World";
		System.out.println("Length:"+greeting.length());//Calculate length
		System.out.println("Uppercase:"+greeting.toUpperCase());//To Uppercase
		System.out.println("Lowercase:"+greeting.toLowerCase());//To Lowercase
		System.out.println("Character at index 1::"+greeting.charAt(1));//To Access character
		String name = "Srushti";
		System.out.println(greeting+" I am "+name);//Concatinating string



	}

}
