package BasicOfJava;

public class RelationalOperator {
    static void main(String[] args) {
        System.out.println(5>3);
        System.out.println(5>=3);
        System.out.println(5<3);
        System.out.println(5*2==10);


//        flag typecasting is not allowed
        boolean x = (2<=5);
        System.out.println(x);

    }
}
