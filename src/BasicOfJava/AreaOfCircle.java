package BasicOfJava;

public class AreaOfCircle {
    static void main(String[] args) {
        //Area of the circle =pi*r*r
        double r ;
        r=20;
        double pi=3.1415;
        double area = pi * r * r;
        System.out.println("Area of the circle is having radius "+ r +" : " + area );
    }
}
