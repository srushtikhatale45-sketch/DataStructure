package BasicOfJava;

public class AreaOfSphere {
    static void main(String[] args) {
        double r;
        double area;
        double pi;
        pi=3.1415;
        r=5;
        area = 4/3 * pi * r * r *r;
        System.out.println("volume of the sphere having radius " + r +" :  "+ area);

    }
}
