package BasicOfJava;

public class BasicArithmetic {
    static void main(String[] args) {
        int x,y;
        x=39;
        y=10;
        System.out.println("Arithmetic operations of integer datatypes");
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        double a,b;
        a=39;
        b=10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println("Arithmetic operations of double datatypes");


    }
}
