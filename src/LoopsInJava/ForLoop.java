package LoopsInJava;

public class ForLoop {
    static void main(String[] args) {
        for(int i=1; i<=10; i++){
            System.out.println("Have a great life ahead");
            System.out.println(i);
        }
        int j;
        for(j=1;j<=8;j++){
            System.out.print(j+" ");
        }
        System.out.println(j);
    }
}
